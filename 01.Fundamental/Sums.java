import java.util.*;

public class Sums
{
	public static void main(String args[]) throws Exception
	{
		Scanner sc = new Scanner(System.in);

		System.out.print("Enter First Number : ");
		int a=sc.nextInt();

		System.out.print("Enter Second number : ");
		int b=sc.nextInt();

		int sum=a+b;

		System.out.println("Sum : "+sum);
	}
}
